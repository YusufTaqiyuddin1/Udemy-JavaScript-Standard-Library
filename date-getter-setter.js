// Date Getter & Setter

const date = new Date();
date.setFullYear(2022);

console.log(date.getFullYear); // [Function: getFullYear]
console.log(date.getMonth); // [Function: getMonth]
console.log(date.getDate); // [Function: getDate]
console.log(date.getHours); // [Function: getHours]
console.log(date.getMilliseconds); // [Function: getMilliseconds]
console.log(date.getTimezoneOffset); // [Function: getTimezoneOffset]